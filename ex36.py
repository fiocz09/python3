"""
Rules for if-statements
1. Every if-statement must have an else.
2. If this else should never run, use a die function, this will find many errors.
3. Never nest if-statements more than two deep and always try to do them one deep.
4. Treat if-statements like paragraphs, where each if-elif-else grouping is like a set of
sentences. Put blank lines before and after.
5. Boolean tests should be simple.
"""

"""
Rules for Loops
1. Use a while-loop only to loop forever.
2. Use a for-loop for all other kinds of looping, especially if there is a fixed or limited number
of things to loop over.
"""

"""
Tips for Debugging
1. Do not use a “debugger.”
2. The best way to debug a program is to use print to see where is wrong.
3. Make sure parts of your programs work as you work on them. Code a little, run a little, fix a little.
"""
