# Ordinal and Cardinal

animals = ['bear', 'python3.6', 'peacock', 'kangaroo', 'whale', 'platypus']

# 1. The animal at 1.
animals[1] # python3.6
# 2. The third (3rd) animal.
animals[3-1] # peacock
# 3. The first (1st) animal.
animals[1-1] # bear
# 4. The animal at 3.
animals[3] # kangaroo
# 5. The fifth (5th) animal.
animals[5-1] # whale
# 6. The animal at 2.
animals[2] # peacock
# 7. The sixth (6th) animal.
animals[6-1] # platypus
# 8. The animal at 4.
animals[4] # whale
